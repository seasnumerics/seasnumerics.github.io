function f = sk(u,g)
global ip im h;
f = ( (u(:,:,:,1)+u(ip,:,:,1)).*g(ip,:,:)...
     -(u(:,:,:,1)+u(im,:,:,1)).*g(im,:,:)...
     +(u(:,:,:,2)+u(:,ip,:,2)).*g(:,ip,:)...
     -(u(:,:,:,2)+u(:,im,:,2)).*g(:,im,:)...
     +(u(:,:,:,3)+u(:,:,ip,3)).*g(:,:,ip)...
     -(u(:,:,:,3)+u(:,:,im,3)).*g(:,:,im))/(4*h);
end